import femm

def addMaterial(mat):
    femm.mi_addmaterial(mat["BlockName"],
        mat["Mu_x"],
        mat["Mu_y"],
        mat["H_c"],
        mat["J_re"],
        mat["Sigma"],
        mat["d_lam"],
        mat["Phi_h"],
        mat["LamFill"],
        mat["LamType"],
        mat["Phi_hx"],
        mat["Phi_hy"],
        mat["NStrands"],
        mat["WireD"])
    if mat["BHPoints"] > 0:
        for i in range(len(mat["BData"])):
            femm.mi_addbhpoint(mat["BlockName"], mat["BData"][i],mat["HData"][i])

